#pragma once

#include "math/vec.h"
//#include "math/mat.h"
#include <vector>

class Object {
public:
    Object() {};
    Object(const char* filePath);
    ~Object();

    const std::vector<vec3f>& GetVertices() const {
        return vertices_;
    }

    const std::vector<uint16_t>& GetTriangles() const {
        return triangles_;
    }

    const vec3f& GetPosition() const {
        return position_;
    }

    void SetPosition(const vec3f newPosition) {
        position_ = newPosition;
    }

    const Color& GetVertexColor() const {
        return vertexColor_;
    }

    void SetVertexColor(const Color newColor) {
        vertexColor_ = newColor;
    }

private:
    vec3f position_{0.0f, 0.0f, 0.0f};
    // vec3f rotation_{0.0f, 0.0f, 0.0f};

    std::vector<vec3f> vertices_;
    std::vector<uint16_t> triangles_;

    Color vertexColor_;
};
